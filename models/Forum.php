<?php
class User {
	
    protected $idUser;
    protected $nome;
	
    

	public function getIdUser() {
		return $this->idUser;
	}

	public function setIduser($idUser {
		$this->idUser = $idUser;
	}
        
    
    public function getNome() {
		return $this->nome;
	}

	public function setNome($nome) {
		$this->nome = $nome;
	}

	
}
